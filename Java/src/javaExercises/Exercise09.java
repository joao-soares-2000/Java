package javaExercises;
//print the volume of a rectangular box
//V = CxLxA
//in cm³
public class Exercise09 {
	public static void main(String[] args) {
		float height = 2.9F;
		float lenght = 3.0F;
		float width = 2.5F;
		float volume = height * lenght * width;
		System.out.println("The volume is " + volume + " cm³");
	}
}
